                    <img src="assets/images/totnghiep.jpg" width="760" height="514">
                    <br>
                    <p>Do ảnh hưởng của dịch Covid-19, Lễ tốt nghiệp và trao bằng kỹ sư, cử nhân học kỳ II năm học 2019 - 2020 đã lùi lại 2 tháng so với kế hoạch dự kiến ban đầu.
                        <br><br> Dịp này có 1.014 sinh viên được công nhận tốt nghiệp, trong đó có 858 sinh viên đại học chính quy. Số sinh viên tốt nghiệp loại Khá trở lên chiếm 44.28%. Cụ thể, Nhà trường ghi nhận 5 sinh viên xếp loại tốt nghiệp xuất
                        sắc, 61 sinh viên Giỏi, 378 sinh viên Khá.
                        <br>
                        <br> Nhờ áp dụng phương thức đào tạo theo học chế tín chỉ đã linh hoạt tạo điều kiện cho sinh viên chủ động sắp xếp, bố trí thời gian học theo nhu cầu của bản thân, sinh viên có thể học vượt hoặc học cùng một lúc 2 chương trình.
                        Trong đợt này có 03 sinh viên Khóa 58 học vượt ra trường sớm 1 kỳ so với thời gian thiết kế, 08 sinh viên được nhận bằng tốt nghiệp văn bằng 2.
                        <br>

                        <br> Trong các sinh viên được nhận bằng tốt nghiệp hôm nay, phần lớn sinh viên tích cực học tập, rèn luyện, nhiều sinh viên có thành tích cao trong học tập và luôn tích cực tham gia các hoạt động phong trào. Điển hình có những
                        sinh viên có kết quả học tập loại xuất sắc như các sinh viên: Đào Thị Huê lớp 58K-PT1 ngành Kinh tế (3.81/4.0) thủ khoa tốt nghiệp; sinh viên Nguyễn Thị Thu Hoài lớp 58QT-TH ngành Quản trị kinh doanh (3.71/4), sinh viên Nguyễn
                        Thị Phương Anh lớp 58KT-DN1 ngành Kế toán (3.70/4); sinh viên Đới Thị Loan lớp 58K-TN ngành Kinh tế (3.69/4), sinh viên Phạm Việt Đức lớp 58KT-DN3 ngành Kế toán (3.65/4)...
                        <br><br> Phát biểu tại buổi lễ tốt nghiệp, GS.TS Trịnh Minh Thụ - Hiệu trưởng đã gửi lời chúc mừng đến các tân kỹ sư, tân cử nhân. Giáo sư khẳng định: thành quả hôm nay không chỉ là nỗ lực của riêng các em sinh viên mà còn là tâm
                        huyết của các thầy giáo, cô giáo, các Phòng ban chức năng, quý phụ huynh đã luôn đồng hành với Trường Đại học Thủy lợi trong sự nghiệp giáo dục “trồng người”. Nhà trường luôn chào đón các em trở lại học tập ở những bậc học cao
                        hơn.
                        <br><br> Đại diện cho các tân kỹ sư, tân cử nhân tốt nghiệp ngày hôm nay, sinh viên Đào Thị Huê lớp 58K-PT1, thủ khoa tốt nghiệp xuất sắc đã gửi lời cảm ơn đến Đảng ủy, Ban giám hiệu, các phòng chức năng và các thầy cô giáo đã
                        luôn quan tâm và tạo điều kiện tốt nhất để sinh viên học tập và rèn luyện. Là một người đạt nhiều thành tích trong học tập, Huê cũng chia sẻ đến các bạn sinh viên hãy luôn đặt mục tiêu trong cuộc sống để có thể nỗ lực cố gắng,
                        phát huy tinh thần tự học, năng lực sáng tạo và hoàn thiện bản thân.

